/**
 * Маппинг typePrefix / названия категории → sourceId (для lookUp в Category).
 */
export function mapCategoryToSourceId(typePrefix: string): string {
  return TYPE_TO_SOURCE_ID[typePrefix] || "10";
}

/**
 * Маппинг ID-префикса Admitad offer (Concert_ → 2) → sourceId.
 */
export function mapAdmitadIdToSourceId(offerId: string): string {
  const prefix = offerId.split("_")[0];
  switch (prefix) {
    case "Concert": return "2";
    case "Theatre": return "3";
    case "Show": return "10";
    case "Exhibition": return "4";
    case "Cinema": return "1";
    case "Quest": return "6";
    case "Sport": return "7";
    case "Excursion": return "8";
    case "Standup": return "9";
    case "Lecture": return "5";
    default: return "10";
  }
}

/**
 * Определяет isKids по typePrefix.
 */
export function isKidsCategory(typePrefix: string): boolean {
  return ["Детский спектакль", "Детям", "до 3 лет", "от 3 до 6 лет", "от 7 до 11 лет", "Новогодние ёлки"].includes(typePrefix);
}
